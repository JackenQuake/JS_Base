'use strict';

let a = 1, b = 1, c, d;

c = ++a;    // Сперва: ++a ~ a = a + 1 = 1 + 1 = 2; Затем: с = а = 2;
alert(c);   // Вывод: 2
d = b++;    // Сперва: d = b = 1; Затем: b++ ~ b = b + 1 = 2; 
alert(d);   // Вывод: 1
c = 2 + ++a;// Сперва: ++a ~ a = a + 1 = 2 + 1 = 3; Затем: c = 2 + a = 2 + 3 = 5; 
alert(c);   // Вывод: 5
d = 2 + b++;// Сперва: d = 2 + b = 2 + 2 = 4; Затем: b++ ~ b = b + 1 = 2 + 1 = 3;
alert(d);   // Вывод: 4
alert(a);   // Вывод: 3
alert(b);   // Вывод: 3